void main() {
  int x = 20;
  int y = 10;

  print(x+y);
  print("x+y : ${x + y}");
  print("x-y : ${x - y}");
  print("x*y : ${x * y}");
  print("x/y : ${x / y}");
  print("x%y : ${x % y}");
  print("x~/y ; ${x ~/ y}");
  print("x++ : ${x++}");
  print("x-- : ${x--}");
  print("--x : ${--x}");
  print("++x - ${++x}");

}